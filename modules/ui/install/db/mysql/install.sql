CREATE TABLE b_ui_avatar_mask_item
(
	ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
	OWNER_TYPE VARCHAR(100) NOT NULL,
	OWNER_ID VARCHAR(20) NOT NULL,
	FILE_ID INT NOT NULL,
	GROUP_ID INTEGER(10),
