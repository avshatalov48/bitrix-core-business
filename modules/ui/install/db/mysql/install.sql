CREATE TABLE b_ui_avatar_mask_access
(
	ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
	ITEM_ID INT(11) NOT NULL,
	ACCESS_CODE VARCHAR(50) NOT NULL,
